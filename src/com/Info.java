package com;

public class Info {
	private String title, inlink, link, text, time, origsrc, replink, repnum;

	public Info() {
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}
	
	public String getInlink() {
		return inlink;
	}
	
	public void setInlink(String inlink) {
		this.inlink = inlink;
	}
	
	public String getLink() {
		return link;
	}

	public void setLink(String link) {
		this.link = link;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}
	
	public String getOrigsrc() {
		return origsrc;
	}
	
	public void setOrigsrc(String origsrc) {
		this.origsrc = origsrc;
	}
	
	public String getReplink() {
		return replink;
	}
	
	public void setReplink(String replink) {
		this.replink = replink;
	}
	
	public String getRepnum() {
		return repnum;
	}
	
	public void setRepnum(String repnum) {
		this.repnum = repnum;
	}
}
